const API_CACHE = new Map(); // 简单内存缓存

/**
 * 查询单个单词的释义
 * @param {string} word - 英文单词（小写）
 * @returns {Promise<{meaning: string, phonetic: string, example: string}|null>}
 */
async function lookup(word) {
  const key = word.toLowerCase().trim();
  if (!key) return null;

  // 1. 内置词库
  if (BUILTIN_DICT[key]) {
    return { meaning: BUILTIN_DICT[key], phonetic: '', example: '' };
  }

  // 2. 内存缓存
  if (API_CACHE.has(key)) {
    return API_CACHE.get(key);
  }

  // 3. Free Dictionary API
  try {
    const resp = await fetch(
      `https://api.dictionaryapi.dev/api/v2/entries/en/${encodeURIComponent(key)}`
    );
    if (!resp.ok) throw new Error('API not found');

    const data = await resp.json();
    if (!Array.isArray(data) || data.length === 0) throw new Error('No data');

    const entry = data[0];
    const phonetic = entry.phonetic || (entry.phonetics && entry.phonetics[0]?.text) || '';
    const meaningObj = entry.meanings && entry.meanings[0];
    const definition = meaningObj?.definitions && meaningObj.definitions[0];
    const meaning = definition?.definition || '';
    const example = definition?.example || '';

    if (!meaning) throw new Error('No definition');

    const result = { meaning, phonetic, example };
    API_CACHE.set(key, result);
    return result;

  } catch {
    API_CACHE.set(key, null); // 避免重复查询
    return null;
  }
}

/**
 * 批量查询（带进度回调）
 * @param {string[]} words - 单词列表
 * @param {(done: number, total: number) => void} onProgress
 * @returns {Promise<Record<string, {meaning: string, phonetic: string, example: string}>>}
 */
async function lookupBatch(words, onProgress) {
  const results = {};
  const BATCH_SIZE = 5;
  const BATCH_DELAY = 400; // ms，避免过快请求

  for (let i = 0; i < words.length; i += BATCH_SIZE) {
    const batch = words.slice(i, i + BATCH_SIZE);
    const promises = batch.map(w => lookup(w).then(r => ({ word: w, result: r })));
    const settled = await Promise.all(promises);

    settled.forEach(({ word, result }) => {
      results[word] = result || { meaning: '', phonetic: '', example: '' };
    });

    if (onProgress) {
      onProgress(Math.min(i + BATCH_SIZE, words.length), words.length);
    }

    if (i + BATCH_SIZE < words.length) {
      await sleep(BATCH_DELAY);
    }
  }

  return results;
}

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

export const dictionaryAPI = {
  lookup,
  lookupBatch,
  // 导出内置词库供其他模块使用
  hasBuiltin: (word) => word.toLowerCase().trim() in BUILTIN_DICT,
  getBuiltin: (word) => BUILTIN_DICT[word.toLowerCase().trim()] || null
};
